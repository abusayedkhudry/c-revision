#include <stdio.h>
int main()
{
    int n;
    scanf("%d", &n);              // সংখ্যাটি ইনপুট নেওয়া হচ্ছে।
    for (int i = 1; i <= 12; i++) // ১ থেকে ১২ পর্যন্ত লুপ চালানো হচ্ছে।
    {
        printf("%d * %d = %d\n", n, i, n * i); // প্রথমে সংখ্যাটি প্রিন্ট হচ্ছে, তারপর পর্যায়ক্রমে ১ থেকে ১২ এবং শেষে এদের গুনফল
    }
    return 0;
}