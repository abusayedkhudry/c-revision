#include <stdio.h>
int main()
{
    int i;
    for (i = 1; i <= 100; i += 1) // লুপ চালানো হচ্ছে ১ থেকে ১০০ পর্যন্ত
    {
        if (i % 2 == 0) // যদি ২ দিয়ে ভাগ করলে ভাগশেষ ০ থাকে তাহলে সেটি জোড়
        {
            printf("%d - Even\n", i); // প্রিন্ট করছি সংখ্যাটি এবং শেষে জোড় লিখে দিচ্ছি
        }
        else // জোড় হওয়ার কন্ডিশন সত্য না হলে
        {
            printf("%d - Odd\n", i); // প্রিন্ট করছি সংখ্যাটি এবং শেষে বিজোড় লিখে দিচ্ছি
        }
    }
    return 0;
}