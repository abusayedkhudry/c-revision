#include <stdio.h>
int main()
{
    int ar[5] = {10, 20, 30, 40, 50}; // ৫ সাইজের এরে ডিক্লেয়ার করে = দিয়ে {} ব্রাকেটের মধ্যে কমা দিয়ে ভেলু দিয়ে দেওয়া হচ্ছে।
    for (int i = 0; i < 5; i++)       // তারপর আমরা এরে প্রিন্ট করে দেখছি ভেলুগুলো ঠিকভাবে প্রিন্ট হচ্ছে কিনা
    {
        printf("%d\n", ar[i]);
    }
    return 0;
}