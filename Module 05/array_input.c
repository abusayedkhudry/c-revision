#include <stdio.h>
int main()
{
    int ar[5];                  // ৫ সাইজের এরে ডিক্লেয়ার করছি
    for (int i = 0; i < 5; i++) // ০ থেকে ৪ পর্যন্ত লুপ চালানো হচ্ছে। কারন ৫ সাইজের এরেতে ইন্ডেক্স থাকে ০ থেকে ৪ পর্যন্ত। 
    {
        scanf("%d", &ar[i]); // প্রতিবার এরের ইন্ডেক্সে ইনপুট নেওয়া হচ্ছে। i এর মান যখন 0 তখন ইনপুট হবে ar[0] তে, i এর মান যখন 1 তখন ইনপুট হবে ar[1] এ। এরকম ভাবে চলবে। 
    }
    return 0;
}