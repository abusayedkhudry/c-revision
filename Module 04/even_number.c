#include <stdio.h>
int main()
{
    int n;
    scanf("%d", &n); // ইনপুট নিচ্ছি
    if (n == 1)      // চেক করছি ইনপুটের মান ১ কিনা
    {
        printf("-1\n"); // ১ হলে -১ প্রিন্ট করছি
    }
    else // ১ নাহলে
    {
        for (int i = 1; i <= n; i = i + 1) // ১ থেকে n পর্যন্ত লুপ চালাচ্ছি
        {
            if (i % 2 == 0) // চেক করছি জোড় কিনা
            {
                printf("%d\n", i); // জোড় হলে প্রিন্ট করছি
            }
        }
    }
    return 0;
}